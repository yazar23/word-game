// Function to get a random subset of words
export const getRandomWords = (count: number): Word[] => {
  const shuffled = [...wordsList].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

// Function to get random incorrect answers for multiple choice
export const getRandomIncorrectAnswers = (correctWord: Word, count: number): string[] => {
  const otherWords = wordsList.filter(word => word.id !== correctWord.id);
  const shuffled = [...otherWords].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count).map(word => word.english);
};